    private void operacion(int i) {
        //instanciar objeto de la clase operaciones
        Operaciones obj = new Operaciones();
        float resultado = 0;
        float n1 = Float.parseFloat(txtN1.getText());
        float n2 = Float.parseFloat(txtN2.getText());
        switch (i) {
            case 1:
                resultado = obj.Sumar(n1, n2);
                break;
            case 2:
                resultado = obj.Restar(n1, n2);
                break;
            case 3:
                resultado = obj.Multiplicar(n1, n2);
                break;
            case 4:
                resultado = obj.Dividir(n1, n2);
                break;
        }
        txtRes.setText(resultado + "");
    }

